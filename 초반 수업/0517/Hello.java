public class Hello {
    public static void main(String[] args) { //메인 메소드(method) {}안부분
        // 쌍따옴표로 감싸져 있는거 => 문자열(String)
        System.out.println("Hello World"); // 문장(statement) 문장의 끝은 ;
        System.out.println("12345");
        System.out.println("010-5039-5692");
    }
}

