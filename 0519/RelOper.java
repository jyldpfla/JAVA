// 관계연산자

public class RelOper {
	public static void main(String[] args) {
		System.out.println(55 == 55); // 두 값이 같은지를 물어보는 연산자 -> 결과값 true,false로 나옴
		System.out.println(55 != 55); // 두 값이 다른지를 물어봄
		System.out.println(55 > 55); // 왼쪽 값이 큰지
		System.out.println(55 < 55); // 오른쪽 값이 큰지
		System.out.println(55 >= 55); // 왼쪽 값이 크거나 같은지
		System.out.println(55 <= 55); // 오른쪽 값이 크거나 같은지
	}
}